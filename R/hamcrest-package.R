
#' Unit tests using the Hamcrest framework
#'
#' The Hamcrest framework for writing unit tests is based on an assertion
#' function and matcher objects. A matcher object implements a single rule that
#' is used by the assertion function to perform the test. This package provides
#' a small set of matcher objects in the form of functions. The functionality
#' provided by this set of matcher objects is identical to those implemented in
#' Renjin, the R interpreter for the Java Virtual Machine. The goal of this
#' package is therefore to be able to run Renjin's unit tests written in R
#' through the GNU R interpreter.
#'
